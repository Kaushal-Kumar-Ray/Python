'''  The isalnum() method returns True only if the entire string only consists of A-Z, a-z, 0-9. 
If any other characters or punctuations are present, then it returns False.'''
str1="HelloWorld12"
print(str1.isalnum())
str2="Hello World"
print(str2.isalnum())
str3="Helloworld!@#"
print(str3.isalnum())